// 국가 코드를 언어 코드로 변환
export function countryToLanguage(country?: string | null): "ko" | "en" | "ja" {
  if (!country) return "ko"; // 기본값은 한국어

  switch (country.toUpperCase()) {
    case "KR":
    case "KOREA":
      return "ko";
    case "US":
    case "USA":
    case "UNITED STATES":
      return "en";
    case "JP":
    case "JAPAN":
      return "ja";
    default:
      return "ko"; // 기본값은 한국어
  }
}

